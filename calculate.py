
class Calc:

    def add(self, a, b):
        print(f" sum of {a} and {b} is : ")
        return int(a) + int(b);

    def multiply(self, a, b):
        print(f" multiplicatio of {a} and {b} is : {a*b}")
        return a*b;

    def division(self, a, b):
        print(f" division of {a} and {b} is : {a/b}")
        return a/b;

    def sub(self, a, b):
        print(f" substraction of {a} and {b} is : {a-b}")
        return a-b;
